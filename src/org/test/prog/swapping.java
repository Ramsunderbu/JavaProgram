package org.test.prog;

public class swapping {
	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("before swapping");
		System.out.println("a is "+a);
		System.out.println("b is "+b);
		
		int c=a;//c=10
		    a=b;//a=20
		    b=c;//b=10
		    System.out.println("after swapping");
			System.out.println("a is "+a);
			System.out.println("b is "+b);
		    
		
	}

}
